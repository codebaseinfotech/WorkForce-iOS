        static func extraLight(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-ExtraLight", size: size)!
        }
        
        static func extraLightItalic(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-ExtraLightItalic", size: size)!
        }
        
        static func semiBold(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-SemiBold", size: size)!
        }
        
        static func semiBoldItalic(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-SemiBoldItalic", size: size)!
        }
        
        static func thin(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-Thin", size: size)!
        }
        
        static func thinItalic(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-ThinItalic", size: size)!
        }
    }
}

