import Foundation
import UIKit

extension UIFont {
    struct AppFont {
        static func black(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-Black", size: size)!
        }
        
        static func blackItalic(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-BlackItalic", size: size)!
        }
        
        static func bold(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-Bold", size: size)!
        }
        
        static func boldItalic(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-BoldItalic", size: size)!
        }
        
        static func extraBold(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-ExtraBold", size: size)!
        }
        
        static func extraBoldItalic(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-ExtraBoldItalic", size: size)!
        }
        
        static func medium(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-Medium", size: size)!
        }
        
        static func mediumItalic(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-MediumItalic", size: size)!
        }
        
        static func regular(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-Regular", size: size)!
        }
        
        static func italic(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-Italic", size: size)!
        }
        
        static func light(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-Light", size: size)!
        }
        
        static func lightItalic(_ size: CGFloat) -> UIFont {
            UIFont(name: "Roboto-LightItalic", size: size)!
